    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        
        String docId = txt_searchDoctor.getText();
        String fname = txt_updateFname.getText();
        String lname = txt_updateLname.getText();
        String contact = txt_updateContactNo.getText();
        String username = txt_updateUsername.getText();

        // Create a User object for the doctor
        User doctor = new User();
        doctor.setUser_id(docId);
        doctor.setFname(fname);
        doctor.setLname(lname);
        doctor.setContact(contact);
        doctor.setUsername(username);

        // Check if doctor ID is empty
        if (docId.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Doctor ID is required.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Validate if contact number is valid (must start with 0 and have exactly 10 digits)
            if (!contact.matches("0[0-9]{9}")) {
                JOptionPane.showMessageDialog(null, "Invalid contact number. It must start with 0 and have exactly 10 digits.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                // Confirmation dialog
                int response = JOptionPane.showConfirmDialog(
                        null, 
                        "Are you sure you want to update the doctor's information?", 
                        "Confirm Update", 
                        JOptionPane.YES_NO_OPTION, 
                        JOptionPane.QUESTION_MESSAGE
                );

                // Proceed if user confirms
                if (response == JOptionPane.YES_OPTION) {
                    // Update doctor information
                    if (controller.updateDoctor(doctor)) {
                        JOptionPane.showMessageDialog(null, "Doctor updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Invalid Doctor ID.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Doctor update canceled.", "Canceled", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }

        // Clear input fields after the process is complete
        txt_searchDoctor.setText("");
        txt_updateFname.setText("");
        txt_updateLname.setText("");
        txt_updateContactNo.setText("");
        txt_updateUsername.setText("");

        // Make fields non-editable after update
        txt_updateFname.setEditable(false);
        txt_updateLname.setEditable(false);
        txt_updateContactNo.setEditable(false);
        txt_updateUsername.setEditable(false); 

        // Refresh the table to reflect changes
        refreshTable();

    }//GEN-LAST:event_btn_saveActionPerformed
