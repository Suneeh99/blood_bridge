    public void refreshTable(){
        DefaultTableModel model = (DefaultTableModel) table_docList.getModel();
        
        List<User> doctors = controller.getDoctors();
        model.setRowCount(0);
    
        for (User doctor : doctors) {
            Vector<Object> row = new Vector<>();
            row.add(doctor.getUser_id());
            row.add(doctor.getFname());
            row.add(doctor.getLname());
            row.add(doctor.getUsername());
            row.add(doctor.getContact());
            model.addRow(row);
        }
    }
